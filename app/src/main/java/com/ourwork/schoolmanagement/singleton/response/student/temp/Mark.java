
package com.ourwork.schoolmanagement.singleton.response.student.temp;

@SuppressWarnings("unused")
public class Mark {


    private String date;

    private String exam;

    private String examID;

    private String subjectID;

    private String subjectMarks;

    private String subjectMaxMarks;

    private String subjectName;

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getExam() {
        return exam;
    }

    public void setExam(String exam) {
        this.exam = exam;
    }

    public String getExamID() {
        return examID;
    }

    public void setExamID(String examID) {
        this.examID = examID;
    }

    public String getSubjectID() {
        return subjectID;
    }

    public void setSubjectID(String subjectID) {
        this.subjectID = subjectID;
    }

    public String getSubjectMarks() {
        return subjectMarks;
    }

    public void setSubjectMarks(String subjectMarks) {
        this.subjectMarks = subjectMarks;
    }

    public String getSubjectMaxMarks() {
        return subjectMaxMarks;
    }

    public void setSubjectMaxMarks(String subjectMaxMarks) {
        this.subjectMaxMarks = subjectMaxMarks;
    }

    public String getSubjectName() {
        return subjectName;
    }

    public void setSubjectName(String subjectName) {
        this.subjectName = subjectName;
    }

}
